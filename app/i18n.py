def t(lang: str) -> dict:
    return STRINGS.get(lang, STRINGS[DEFAULT_LANGUAGE])


def resolve_lang(candidate: str | None) -> str:
    if candidate in LANGUAGES:
        return candidate
    return DEFAULT_LANGUAGE


def from_request(request) -> str:
    return resolve_lang(request.cookies.get("lang"))
