export type Strings = Record<StringKey, string>;

/** Look up a string, falling back to the key itself so nothing renders blank. */
export function makeTranslator(strings: Partial<Strings>) {
  return (key: StringKey): string => strings[key] ?? key;
}

export type Translate = ReturnType<typeof makeTranslator>;
